CREATE TABLE IF NOT EXISTS log_login (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
    user_id BIGINT DEFAULT NULL COMMENT '用户ID',
    username VARCHAR(64) DEFAULT NULL COMMENT '用户名',
    success_flag TINYINT NOT NULL COMMENT '登录结果：1成功，0失败',
    message VARCHAR(255) DEFAULT NULL COMMENT '登录结果说明',
    ip VARCHAR(64) DEFAULT NULL COMMENT '客户端IP',
    user_agent VARCHAR(512) DEFAULT NULL COMMENT '客户端信息',
    login_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '登录时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='登录日志表';

CREATE TABLE IF NOT EXISTS log_operation (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
    user_id BIGINT DEFAULT NULL COMMENT '操作人ID',
    username VARCHAR(64) DEFAULT NULL COMMENT '操作人用户名',
    operation_module VARCHAR(64) NOT NULL COMMENT '操作模块',
    operation_type VARCHAR(64) NOT NULL COMMENT '操作类型',
    request_uri VARCHAR(255) DEFAULT NULL COMMENT '请求URI',
    request_method VARCHAR(16) DEFAULT NULL COMMENT '请求方法',
    request_body TEXT COMMENT '请求摘要',
    response_body TEXT COMMENT '响应摘要',
    success_flag TINYINT NOT NULL COMMENT '执行结果：1成功，0失败',
    error_message VARCHAR(512) DEFAULT NULL COMMENT '错误信息',
    trace_id VARCHAR(64) DEFAULT NULL COMMENT '链路追踪ID',
    operation_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '操作时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='操作日志表';

